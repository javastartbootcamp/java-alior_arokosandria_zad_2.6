package pl.javastart.task;

public class Methods {

    public boolean isEven(int a) {
        return a % 2 == 0;
    }

    public boolean isOdd(int b) {
        return b % 2 != 0;
    }

    public double circleField(double r) {
        return Math.PI * r * r;
    }

    public int power(int c) {
        return c * c;
    }
}
